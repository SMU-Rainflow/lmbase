"""
Interface of the CodeForces-CoTS dataset.

Dataset Source: https://huggingface.co/datasets/open-r1/codeforces-cots

Description:
    A large-scale dataset for training reasoning models on competitive programming
    tasks. Consists of 10k CodeForces problems with up to five reasoning traces
    generated by DeepSeek R1. Around 84% of the Python solutions pass public tests.

Size:
    - solutions: 47,780 examples
    - solutions_py: 9,556 examples (Python-only)
    - checker_interactor: 35,718 examples
    - solutions_decontaminated: 40,665 examples
    - solutions_py_decontaminated: 8,133 examples

Configurations (subset field):
    - solutions: All solutions with multiple languages
    - solutions_py: Python-only solutions (default, recommended)
    - checker_interactor: Problems with checker/interactor
    - solutions_decontaminated: Decontaminated solutions
    - solutions_py_decontaminated: Decontaminated Python-only solutions
    Config setting in code: subset="solutions_py" (codeforcescots.py 38-39, 55)

Splits:
    - train: Training set (varies by configuration)

Features:
    - id: Problem identifier
    - title: Problem title
    - description: Problem description
    - input_format: Input specification
    - output_format: Output specification
    - examples: List of input/output test cases
    - prompt: Full prompt used for DeepSeek R1
    - generation: R1 output containing <think>...</think> and code
    - finish_reason: Completion reason
    - api_metadata: Token usage information

Generation field parsing (codeforcescots.py 70-85):
    The `generation` field contains DeepSeek R1's output in the format:
        <think>[chain-of-thought reasoning]</think>\n[code solution]
    Parsing logic:
        - Content between <think> and </think> -> cot_answer
        - Content after </think> -> groundtruth (code solution)

License: CC BY 4.0
Paper: Open-R1 (https://huggingface.co/open-r1)
"""

import logging
from datasets import load_dataset, config as hf_config

from lmbase.dataset.base import TextCodeSample, VisualTextBase

# Valid HuggingFace config names for this dataset
VALID_SUBSETS = {
    "solutions",
    "solutions_py",
    "checker_interactor",
    "solutions_decontaminated",
    "solutions_py_decontaminated",
}


class CodeForcesCoTSDataset(VisualTextBase):
    """A consistent interface for the CodeForces-CoTS dataset."""

    def __init__(self, split="train", **kwargs):
        config = kwargs.get("config", {})
        # Default to solutions_py (Python-only, most practical)
        self.subset = config.get("subset", "solutions_py")
        if self.subset not in VALID_SUBSETS:
            raise ValueError(
                f"Invalid subset '{self.subset}'. "
                f"Valid options: {sorted(VALID_SUBSETS)}"
            )
        super().__init__(split=split, **kwargs)

    def map_dataset(self):
        """Map HF dataset rows to standardized sample records."""
        if self.hf_dataset is None:
            logging.info("   - HF cache directory: %s", hf_config.HF_DATASETS_CACHE)
            self.hf_dataset = load_dataset(
                self.hf_dataname,
                self.subset,
                split=self.split,
            )

        logging.info(
            "   - Mapping samples to lmbase format, i.e., lmbase.dataset.base.TextCodeSample"
        )
        self.hf_dataset = self.hf_dataset.map(
            self.batch_format,
            batched=True,
            batch_size=1000,
            load_from_cache_file=True,
            remove_columns=self.hf_dataset.column_names,
        )
        self.save_example_samples(num_samples=20)

    @staticmethod
    def _parse_generation(generation: str):
        """Parse generation field into CoT reasoning and code solution.

        The generation field format from DeepSeek R1:
            <think>[reasoning]</think>\n[code]

        Returns:
            tuple: (cot_answer, groundtruth_code)
        """
        if not generation:
            return "", ""

        if "</think>" in generation:
            parts = generation.split("</think>", 1)
            # Extract CoT (remove leading <think> tag)
            cot = parts[0].replace("<think>", "", 1).strip()
            code = parts[1].strip()
            return cot, code

        # No </think> tag: entire content is treated as code
        return "", generation.strip()

    def to_format(self, sample):
        """Convert raw sample to standardized format.

        Field mapping (codeforcescots.py 88-117):
            id         -> main_id
            prompt     -> question
            generation -> cot_answer (<think>...</think> part) + groundtruth (code part)
            examples   -> test_cases
            id/title/contest_id -> sample_info
        """
        self.idx += 1

        # Use prompt as the question (it's the formatted problem)
        question = sample["prompt"]
        question = f"{question}{self.SOLUTION_FORMAT_PROMPT}"

        # Parse generation into CoT and code
        cot_answer, groundtruth = self._parse_generation(sample["generation"])

        # Extract test cases from examples
        examples = sample.get("examples") or []
        test_cases = [{"input": ex["input"], "output": ex["output"]} for ex in examples]

        return TextCodeSample(
            main_id=sample["id"],
            split=self.split,
            question=question,
            cot_answer=cot_answer,
            groundtruth=groundtruth,
            test_cases=test_cases,
            sample_info={
                "dataset": self.hf_dataname,
                "subset": self.subset,
                "title": sample.get("title", ""),
                "contest_id": sample.get("contest_id", ""),
                "contest_name": sample.get("contest_name", ""),
                "finish_reason": sample.get("finish_reason", ""),
            },
        )
